# Operadores aritméticos
# Soma
soma = 10 + 5
print(soma)
# Subtração
Subtracao = 10 - 5
print(Subtracao)
# Multiplicação
multiplicacao = 10 * 5
print(multiplicacao)
# Divisão
divisao = 10/5
print(divisao)
# Divisão inteira
divisao_inteiro = 10 // 5
print(divisao_inteiro)
# Exponenciação
exponenciacao = 10 ** 5
print (exponenciacao)
# Módulo / Resto da Divisão
modulo = 10 % 5
print(modulo)